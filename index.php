        <!-- Abstract -->
        <h3 class="w3-animate-zoom w3-black w3-center " id="Abstractnss" style="font: 16px/20px Georgia, &#39;Times New Roman&#39;, Times, serif"><u>Abstract</u></h3>
       
        <p> The National Service Scheme (NSS) is an Indian government-sponsored public service program conducted by the Ministry of Youth Affairs and Sports of the Government of India. Popularly known as NSS, the scheme was launched in Gandhiji's Centenary year in 1969. Aimed at developing student's personality through community service, NSS is a voluntary association of young people in Colleges, Universities and at +2 level working for a campus-community (esp. Villages) linkage.<br>
           NSS volunteers work in rural areas, adopted villages and school to the serving the cause of society through survey, education and health awareness programme. This study involves in identifying the importance, aims and objectives of NSS in higher education which in turn create social responsibility among the students.</p>
 
 
 
        <!-- History of NSS -->
        <h3 class="w3-animate-zoom w3-black w3-center " id="historynss" style="font: 16px/20px Georgia, &#39;Times New Roman&#39;, Times, serif"><u>HISTORY OF NSS</u></h3>
        <p style="margin-left: 16%">
           </p><p>National Service Scheme (NSS) was launched during 1969, the birth centenary year of Mahatma Gandhi, in 37 universities involving 40000 students. NSS is an extension dimension to the higher education system to orient the student youth to community service while they are studying in educational institutions. It is being implemented by the Ministry of Youth Affairs and Sports, Government of India.
 
         </p>
         

